/* cJSON 结构 */
/*说明：
* 1、cJSON是使用链表来存储数据的，其访问方式很像一颗树。每一个节点可以有兄弟节点，通过next/prev指针来查找，它类似双向链表；
* 每个节点也可以有子节点，通过child指针来访问，进入下一层。只有节点是对象或数组时才可以有子节点。
* 2、type是键（key）的类型，一共有7种取值，分别是：False，Ture，NULL，Number，String，Array，Object。
* 若是Number类型，则valueint或valuedouble中存储着值。
* 若期望的是int，则访问valueint，若期望的是double，则访问valuedouble，可以得到值。
* 若是String类型的，则valuestring中存储着值，可以访问valuestring得到值。
* 3、string中存放的是这个节点的名字，可理解为key的名称。
*/
typedef struct cJSON
{
    /* 遍历数组或对象链的前向或后向链表指针*/
    struct cJSON *next;
    struct cJSON *prev;
    /*数组或对象的子节点*/
    struct cJSON *child;

    /* key的类型*/
    int type;

    /*字符串值*/
    char *valuestring;
    /* 整数值*/
    int valueint;
    /* 浮点数值*/
    double valuedouble;

    /* key的名字*/
    char *string;
} cJSON;

/* json内存管理 */
/* 为方便用户自由的管理内存，其使用了Hook技术让使用者可以自定义内存管理函数 */
